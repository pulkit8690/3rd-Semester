/* PROGRAM TO PRINT MARKS OF THE STUDENTS */


#include <stdio.h>
int main()
{
    int marks[] = {1, 2, 3, 4, 5, 6};
    printf("Marks of 1 student %d\n", marks[0]);
    printf("Marks of 2 student %d\n", marks[1]);
    printf("Marks of 3 student %d\n", marks[2]);
    printf("Marks of 4 student %d\n", marks[3]);
    printf("Marks of 5 student %d\n", marks[4]);
    return 0;
}